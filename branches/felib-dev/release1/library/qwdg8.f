      SUBROUTINE QWDG8(WGHT, IWGHT, ABSS, IABSS, JABSS, NQP, ITEST)
      INTEGER I, IABSS, ITEST, IWGHT, JABSS, NQP, IERROR, ERRMES
      DOUBLE PRECISION ABSS, WGHT, SRNAME
      DIMENSION ABSS(IABSS,JABSS), WGHT(IWGHT)
      DATA SRNAME /8H QWDG8  /
      NQP = 8
      IF(ITEST.EQ.-1) GO TO 999
      IERROR=0
      IF(IWGHT.LT.NQP) IERROR=1
      IF(IABSS.LT.3.OR.JABSS.LT.NQP) IERROR=2
      ITEST=ERRMES(ITEST,IERROR,SRNAME)
      IF(ITEST.NE.0) RETURN
999   WGHT(1) = 3.0D0/8.0D0
      WGHT(2) = 3.0D0/8.0D0
      DO 1010 I=3,8
      WGHT(I) = 1.0D0/24.0D0
 1010 CONTINUE
      ABSS(1,1) = 0.0D0
      ABSS(2,1) = 0.0D0
      ABSS(3,1) = 1.0D0
      ABSS(1,2) = 0.0D0
      ABSS(2,2) = 0.0D0
      ABSS(3,2) = -1.0D0
      ABSS(1,3) = 1.0D0
      ABSS(2,3) = 0.0D0
      ABSS(3,3) = -1.0D0
      ABSS(1,4) = -0.5D0
      ABSS(2,4) = DSQRT(3.0D0)/2.0D0
      ABSS(3,4) = -1.0D0
      ABSS(1,5) = -0.5D0
      ABSS(2,5) = -ABSS(2,4)
      ABSS(3,5) = -1.0D0
      ABSS(1,6) = 1.0D0
      ABSS(2,6) = 0.0D0
      ABSS(3,6) = 1.0D0
      ABSS(1,7) = -0.5D0
      ABSS(2,7) = ABSS(2,4)
      ABSS(3,7) = 1.0D0
      ABSS(1,8) = -0.5D0
      ABSS(2,8) = ABSS(2,5)
      ABSS(3,8) = 1.0D0
      RETURN
      END
