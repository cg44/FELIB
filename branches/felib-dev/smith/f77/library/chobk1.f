      SUBROUTINE CHOBK1(KB,IKB,LOADS,N,IW)
C
C      THIS SUBROUTINE PERFORMS CHOLESKI FORWARD SUBSTITUTION
C
      REAL KB(IKB,*),LOADS(*)
      LOADS(1)=LOADS(1)/KB(1,IW+1)
      DO 1 I=2,N
      X=0.
      K=1
      IF(I.LE.IW+1)K=IW-I+2
      DO 2 J=K,IW
    2 X=X+KB(I,J)*LOADS(I+J-IW-1)
    1 LOADS(I)=(LOADS(I)-X)/KB(I,IW+1)
      RETURN
      END
