      SUBROUTINE FMTRI3(DER,IDER,FUN,SAMP,ISAMP,I)
C
C      THIS SUBROUTINE FORMS THE SHAPE FUNCTIONS AND
C      THEIR DERIVATIVES FOR 3-NODED TRIANGULAR ELEMENTS
C
      REAL DER(IDER,*),SAMP(ISAMP,*),FUN(*)
      FUN(1)=SAMP(I,1)
      FUN(2)=SAMP(I,2)
      FUN(3)=1.-FUN(1)-FUN(2)
      DER(1,1)=1.
      DER(1,2)=0.
      DER(1,3)=-1.
      DER(2,1)=0.
      DER(2,2)=1.
      DER(2,3)=-1.
      RETURN
      END
