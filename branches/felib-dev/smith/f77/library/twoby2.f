      SUBROUTINE TWOBY2(JAC,IJAC,JAC1,IJAC1,DET)
C
C      THIS SUBROUTINE FORMS THE INVERSE OF A 2 BY 2 MATRIX
C
      REAL JAC(IJAC,*),JAC1(IJAC1,*)
      DET=JAC(1,1)*JAC(2,2)-JAC(1,2)*JAC(2,1)
      JAC1(1,1)=JAC(2,2)
      JAC1(1,2)=-JAC(1,2)
      JAC1(2,1)=-JAC(2,1)
      JAC1(2,2)=JAC(1,1)
      DO 1 K=1,2
      DO 1 L=1,2
    1 JAC1(K,L)=JAC1(K,L)/DET
      RETURN
      END
