      SUBROUTINE FMBEAM(DER2,FUN,SAMP,ISAMP,ELL,I)
C
C      THIS SUBROUTINE FORMS THE BEAM SHAPE FUNCTIONS
C      AND THEIR 2ND DERIVATIVES IN LOCAL COORDINATES
C
      REAL DER2(*),FUN(*),SAMP(ISAMP,*)
      XI=SAMP(I,1)
      XI2=XI*XI
      XI3=XI2*XI
      FUN(1)=.25*(XI3-3.*XI+2.)
      FUN(2)=.125*ELL*(XI3-XI2-XI+1.)
      FUN(3)=.25*(-XI3+3.*XI+2.)
      FUN(4)=.125*ELL*(XI3+XI2-XI-1.)
      DER2(1)=1.5*XI
      DER2(2)=.25*ELL*(3.*XI-1.)
      DER2(3)=-1.5*XI
      DER2(4)=.25*ELL*(3.*XI+1.)
      RETURN
      END
