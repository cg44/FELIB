      SUBROUTINE FRMUPV(KE,IKE,C11,IC11,C12,IC12,C21,IC21,
     +C23,IC23,C32,IC32,NOD,NODF,ITOT)
C
C      THIS SUBROUTINE FORMS THE UNSYMMETRICAL STIFFNESS MATRIX
C      FOR THE U-V-P VERSION OF THE NAVIER STOKES EQUATIONS
C
      REAL KE(IKE,*),C11(IC11,*),C21(IC21,*),C23(IC23,*),
     +C32(IC32,*),C12(IC12,*)
      K=NOD+NODF
      DO 1 I=1,NOD
      DO 1 J=1,NOD
    1 KE(I,J)=C11(I,J)
      DO 2 I=1,NOD
      DO 2 J=NOD+1,K
    2 KE(I,J)=C12(I,J-NOD)
      DO 3 I=NOD+1,K
      DO 3 J=1,NOD
    3 KE(I,J)=C21(I-NOD,J)
      DO 4 I=NOD+1,K
      DO 4 J=K+1,ITOT
    4 KE(I,J)=C23(I-NOD,J-K)
      DO 5 I=K+1,ITOT
      DO 5 J=NOD+1,K
    5 KE(I,J)=C32(I-K,J-NOD)
      DO 6 I=K+1,ITOT
      DO 6 J=K+1,ITOT
    6 KE(I,J)=C11(I-K,J-K)
      RETURN
      END
