      SUBROUTINE VOL2D(BEE,IBEE,VOL,NOD)
C
C      THIS SUBROUTINE FORMS A VECTOR CONTAINING THE
C      DERIVATIVES OF THE SHAPE FUNCTIONS (PLANE 2-D)
C
      REAL BEE(IBEE,*),VOL(*)
      DO 1 M=1,NOD
      K=2*M
      L=K-1
      VOL(L)=BEE(1,L)
      VOL(K)=BEE(2,K)
    1 CONTINUE
      RETURN
      END
