      PROGRAM P36
C
C      PROGRAM 3.6 ITERATIVE SUBSTITUTION FOR MANY EQUATIONS
C
C      ALTER NEXT LINE TO CHANGE PROBLEM SIZE
C
      PARAMETER (IN=20)
C
      REAL X0(IN),X1(IN)
C
      READ (5,*) N,(X0(I),I=1,N)
      READ (5,*) TOL,ITS
      WRITE(6,*) ('** SIMPLE ITERATION FOR A SYSTEM OF EQUATIONS **')
      WRITE(6,*)
      WRITE(6,*) ('INITIAL VALUES')
      CALL PRINTV(X0,N,6)
      WRITE(6,*)
      ITERS = 0
      ICON = 0
    1 CALL F(X0,X1)
      CALL CHECON(X1,X0,N,TOL,ICON)
      ITERS = ITERS + 1
      IF (ICON.EQ.0 .AND. ITERS.LT.ITS) GO TO 1
      WRITE(6,*) ('SOLUTIONS AND ITERATIONS TO CONVERGENCE')
      CALL PRINTV(X1,N,6)
      WRITE(6,100) ITERS
  100 FORMAT (I10)
      STOP
      END
C
      SUBROUTINE F(X0,X1)
C
C      THIS SUBROUTINE PROVIDES THE VALUES OF THE FUNCTIONS
C      OF X AND WILL VARY FROM ONE PROBLEM TO THE NEXT
C
C
      REAL X0(*),X1(*)
C
      X1(1) = SQRT(2.16-0.5*X0(2)*X0(2))
      X1(2) = X0(1)
      RETURN
      END
