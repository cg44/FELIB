      PROGRAM P35
C
C      PROGRAM 3.5 MODIFIED NEWTON-RAPHSON FOR SINGLE ROOT
C
      READ (5,*) X0,TOL,ITS
      WRITE(6,*) ('****** MODIFIED NEWTON-RAPHSON METHOD ***********')
      WRITE(6,*)
      WRITE(6,*) ('INITIAL VALUE')
      WRITE (6,100) X0
      WRITE(6,*)
      WRITE(6,*) ('VALUE OF X AT EACH ITERATION')
      ITERS = 0
      ICON = 0
      FD = FDASH(X0)
    1 X1 = X0 - F(X0)/FD
      CALL CHECK(X1,X0,TOL,ICON)
      ITERS = ITERS + 1
      WRITE(6,100)X1,ITERS
      IF (ICON.EQ.0 .AND. ITERS.LT.ITS) GO TO 1
      WRITE(6,*)
      WRITE(6,*) ('SOLUTION AND ITERATIONS TO CONVERGENCE')
      WRITE (6,100) X1,ITERS
  100 FORMAT (E12.4,I10)
      STOP
      END
C
      FUNCTION F(X)
C
C      THIS FUNCTION PROVIDES THE VALUE OF F(X)
C      AND WILL VARY FROM ONE PROBLEM TO THE NEXT
C
C
      F = X**3 - X - 1.0
      RETURN
      END
C
      FUNCTION FDASH(X)
C
C      THIS FUNCTION PROVIDES THE VALUE OF DF/DX
C      AND WILL VARY FROM ONE PROBLEM TO THE NEXT
C
C
      FDASH = 3.0*X**2 - 1.0
      RETURN
      END
