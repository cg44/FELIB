      SUBROUTINE BMCOL2(KM,EA,EI,IP,COORD,ICOORD)
C
C      THIS SUBROUTINE FORMS THE STIFFNESS MATRIX OF AN
C      INCLINED 2-D BEAM-COLUMN ELEMENT
C
      REAL KM(6,6),COORD(ICOORD,*)
      X1=COORD(IP,1)
      Y1=COORD(IP,2)
      X2=COORD(IP,3)
      Y2=COORD(IP,4)
      ELL=SQRT((Y2-Y1)**2+(X2-X1)**2)
      C=(X2-X1)/ELL
      S=(Y2-Y1)/ELL
      E1=EA/ELL
      E2=12.*EI/(ELL*ELL*ELL)
      E3=EI/ELL
      E4=6.*EI/(ELL*ELL)
      KM(1,1)=C*C*E1+S*S*E2
      KM(4,4)=KM(1,1)
      KM(1,2)=S*C*(E1-E2)
      KM(2,1)=KM(1,2)
      KM(4,5)=KM(1,2)
      KM(5,4)=KM(4,5)
      KM(1,3)=-S*E4
      KM(3,1)=KM(1,3)
      KM(1,6)=KM(1,3)
      KM(6,1)=KM(1,6)
      KM(3,4)=S*E4
      KM(4,3)=KM(3,4)
      KM(4,6)=KM(3,4)
      KM(6,4)=KM(4,6)
      KM(1,4)=-KM(1,1)
      KM(4,1)=KM(1,4)
      KM(1,5)=S*C*(-E1+E2)
      KM(5,1)=KM(1,5)
      KM(2,4)=KM(1,5)
      KM(4,2)=KM(2,4)
      KM(2,2)=S*S*E1+C*C*E2
      KM(5,5)=KM(2,2)
      KM(2,5)=-KM(2,2)
      KM(5,2)=KM(2,5)
      KM(2,3)=C*E4
      KM(3,2)=KM(2,3)
      KM(2,6)=KM(2,3)
      KM(6,2)=KM(2,6)
      KM(3,3)=4.*E3
      KM(6,6)=KM(3,3)
      KM(3,5)=-C*E4
      KM(5,3)=KM(3,5)
      KM(5,6)=KM(3,5)
      KM(6,5)=KM(5,6)
      KM(3,6)=2.*E3
      KM(6,3)=KM(3,6)
      RETURN
      END
