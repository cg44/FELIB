      SUBROUTINE FORMKC(BK,KM,IKM,CM,ICM,G,N,IDOF)
C
C      THIS SUBROUTINE ASSEMBLES COMPLEX ELEMENT MATRICES INTO A
C      SYMMETRICAL BAND GLOBAL MATRIX(STORED AS A VECTOR)
C
      COMPLEX BK(*)
      REAL KM(IKM,*),CM(ICM,*)
      INTEGER G(*)
      DO 1 I=1,IDOF
      IF(G(I).EQ.0) GO TO 1
    2 DO 5 J=1,IDOF
      IF(G(J).EQ.0) GO TO 5
    3 ICD=G(J)-G(I)+1
      IF(ICD-1)5,4,4
    4 IVAL=N*(ICD-1)+G(I)
      BK(IVAL)=BK(IVAL)+CMPLX(KM(I,J),CM(I,J))
    5 CONTINUE
    1 CONTINUE
      RETURN
      END
