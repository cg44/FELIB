      SUBROUTINE BNONAX(BEE,IBEE,DERIV,IDERIV,FUN,COORD,ICOORD,
     +                  SUM,NOD,IFLAG,LTH)
C
C      THIS SUBROUTINE FORMS THE STRAIN-DISPLACEMENT MATRIX FOR
C      AXISYMMETRIC SOLIDS SUBJECTED TO NON-AXISYMMETRIC LOADING
C
      REAL BEE(IBEE,*),DERIV(IDERIV,*),FUN(*),COORD(ICOORD,*)
      SUM=0.
      DO 1 K=1,NOD
    1 SUM=SUM+FUN(K)*COORD(K,1)
      DO 2 M=1,NOD
      N=3*M
      K=N-1
      L=K-1
      BEE(1,L)=DERIV(1,M)
      BEE(2,K)=DERIV(2,M)
      BEE(3,L)=FUN(M)/SUM
      BEE(3,N)=IFLAG*LTH*BEE(3,L)
      BEE(4,L)=DERIV(2,M)
      BEE(4,K)=DERIV(1,M)
      BEE(5,K)=-IFLAG*LTH*FUN(M)/SUM
      BEE(5,N)=DERIV(2,M)
      BEE(6,L)=BEE(5,K)
    2 BEE(6,N)=DERIV(1,M)-FUN(M)/SUM
      RETURN
      END
