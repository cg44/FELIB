      SUBROUTINE FORMTB(KB,IKB,KM,IKM,G,IW,IDOF)
C
C      THIS SUBROUTINE ASSEMBLES THE ELEMENT MATRICES INTO AN
C      UNSYMMETRICAL BANDED MATRIX 'KB'.
C
      REAL KB(IKB,*),KM(IKM,*)
      INTEGER G(*)
      DO 1 I=1,IDOF
      IF(G(I).EQ.0)GOTO 1
      DO 2 J=1,IDOF
      IF(G(J).EQ.0)GOTO 2
      ICD=G(J)-G(I)+IW+1
      KB(G(I),ICD)=KB(G(I),ICD)+KM(I,J)
    2 CONTINUE
    1 CONTINUE
      RETURN
      END
