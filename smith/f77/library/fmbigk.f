      SUBROUTINE FMBIGK(BIGK,IBIGK,KM,IKM,G,IDOF)
C
C       THIS SUBROUTINE ASSEMBLES ELEMENT MATRICES INTO A
C       FULL GLOBAL MATRIX
C
      REAL BIGK(IBIGK,*),KM(IKM,*)
      INTEGER G(*)
      DO 1 I=1,IDOF
      IF(G(I).EQ.0) GO TO 1
    2 DO 3 J=1,IDOF
      IF(G(J).EQ.0) GO TO 3
    4 BIGK(G(I),G(J))=BIGK(G(I),G(J))+KM(I,J)
    3 CONTINUE
    1 CONTINUE
      RETURN
      END
