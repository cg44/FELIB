      SUBROUTINE GEO4X1(IP,IQ,NXE,AA,BB,COORD,ICOORD,G,NF,INF)
C
C      THIS SUBROUTINE FORMS THE COORDINATES AND STEERING VECTOR
C      FOR 4-NODE QUADS COUNTING IN X-DIRECTION
C      LAPLACE'S EQUATION   1-FREEDOM PER NODE
C
      REAL COORD(ICOORD,*)
      INTEGER NUM(4),G(*),NF(INF,*)
      NUM(1)=IQ*(NXE+1)+IP
      NUM(2)=(IQ-1)*(NXE+1)+IP
      NUM(3)=NUM(2)+1
      NUM(4)=NUM(1)+1
      DO 1 I=1,4
    1 G(I)=NF(NUM(I),1)
      COORD(1,1)=(IP-1)*AA
      COORD(2,1)=(IP-1)*AA
      COORD(3,1)=IP*AA
      COORD(4,1)=IP*AA
      COORD(1,2)=-IQ*BB
      COORD(2,2)=-(IQ-1)*BB
      COORD(3,2)=-(IQ-1)*BB
      COORD(4,2)=-IQ*BB
      RETURN
      END
