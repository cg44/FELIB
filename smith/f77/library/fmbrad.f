      SUBROUTINE FMBRAD(BEE,IBEE,DERIV,IDERIV,FUN,COORD,ICOORD,SUM,NOD)
C
C      THIS SUBROUTINE FORMS THE STRAIN/DISPLACEMENT MATRIX
C      FOR AXISYMMETRIC STRAIN
C
      REAL BEE(IBEE,*),DERIV(IDERIV,*),FUN(*),COORD(ICOORD,*)
      SUM=0.
      DO 1 I=1,NOD
    1 SUM=SUM+FUN(I)*COORD(I,1)
      DO 2 M=1,NOD
      K=2*M
      L=K-1
      X=DERIV(1,M)
      BEE(1,L)=X
      BEE(3,K)=X
      Y=DERIV(2,M)
      BEE(2,K)=Y
      BEE(3,L)=Y
      BEE(4,L)=FUN(M)/SUM
    2 CONTINUE
      RETURN
      END
