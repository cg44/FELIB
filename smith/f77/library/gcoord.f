      SUBROUTINE GCOORD(FUN,COORD,ICOORD,NOD,IT,GC)
C
C      THIS SUBROUTINE OBTAINS THE CARTESIAN COORDINATES OF THE
C      GAUSS-POINTS FROM THE SHAPE FUNCTIONS
C
      REAL GC(*),FUN(*),COORD(ICOORD,*)
      DO 1 I=1,IT
      GC(I)=0.
      DO 1 J=1,NOD
    1 GC(I)=GC(I)+COORD(J,I)*FUN(J)
      RETURN
      END
